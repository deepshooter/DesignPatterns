package com.deepshooter.designpatterns.creational.factory

/**
 *
 * -----Factory-----
 *
 * -Provides a way to access functionality without caring about implementation
 * -Separation of Concerns
 * -Allows for Testability
 *
 *
 * -> When there is a super class and multiple sub classes an dwe want to get the object of sub
 * classes based on input and requirement. Then we create factory class which takes the
 * responsibility of creating object of class on based of input.
 *
 * Advantages of Factory Design Pattern:
 * 1. Focus on creating object for interface rather than implementation.
 * 2. Loose Coupling
 * 3. More Robust Code
 *
 *
 */